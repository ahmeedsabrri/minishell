#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	int		i;
	int		j;
	char	*str;

	i = 0;
	j = 0;
	if (!s1 && !s2)
		return (NULL);
	if(!s1)
		return(ft_strdup(s2));
	if(!s2)
		return(ft_strdup(s1));
	str = ft_malloc(((ft_strlen(s1) + ft_strlen(s2)) + 1) * sizeof(char), 1);
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		str[i + j] = s2[j];
		j++;
	}
	str[i + j] = '\0';
	return (str);
}

